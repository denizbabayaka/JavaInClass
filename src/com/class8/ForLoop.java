package com.class8;

public class ForLoop {

	public static void main(String[] args) {
		
		
		for(int i=0;i<4;i++) {              //best choice to use when #      
			System.out.println("i");     
		}

	for(int i=1;i<=10;i++){       // we can use same variable inside the for loop  
		System.out.println(i);
	}
	
	for (int c=10;c>1;c--) {
		System.out.println(c);
	}
	
	for(int s=0;s<=50;s+=5) {
		System.out.println(s);
	}
	
	for (int i=1;i<=100;i++) {
		System.out.println(i);
	}
	
	for(int i=100;i>=1;i--) {
		System.out.println(i);
	}
	
	
	for(int i=20;i>=1;i-=2) {
		System.out.println(i);
	}
	
	for(int i=2;i<=20;i+=2) {
		System.out.println(i);
	}
	

	
	
	
	
	}

}
