package com.class9;

public class Pattern2 {
	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) {
			for (int y = 1; y <= i; y++) {
				System.out.print(y);
			}
			System.out.println();
		}

		
		
		for (int i = 1; i <= 9; i++) {   
			for (int y = 1; y <= i; y++) { // every time y checks  the condition of i which increment from the outer loop and then compares
				System.out.print(i);
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
