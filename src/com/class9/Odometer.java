package com.class9;

public class Odometer {
	public static void main(String[] args) {

		for (int i = 0; i < 10; i++) {

			for (int y = 0; y < 10; y++) {

				for (int c = 0; c < 10; c++) {

					for (int d = 0; d < 10; d++) {
						System.out.println(i + "" + y + "" + c + "" + d);
					}
				}
			}

		}

	}
}
