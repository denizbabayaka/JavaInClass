package com.class3;

public class ShorthandOperators {

	public static void main(String[] args) {
		
		int num1=1000;
		num1+=1000;            //num1=num3+1000; operators come before the equal
		System.out.println(num1);

		
		int num2=50;
		num2+=20;  //num2=num2+20;
		num2-=30;  //num2=70-30;
		num2/=10;  //num2=40/10;
		
		num2%=2;   // num2=4%2;
		
		System.out.println(num2);   
		System.out.println("***************");
		int num3=20;  //Task 1  Shorthand example
		num3+=100;
		num3-=67;
		
		double cakepiece=11;
		cakepiece/=4;
		
		int cakepiece2=25;
		cakepiece2%=7;
		System.out.println(num3);
		System.out.println(cakepiece);
		System.out.println(cakepiece2);
		
		
		
		
		

		
		
	}
	
	
	
	

}
